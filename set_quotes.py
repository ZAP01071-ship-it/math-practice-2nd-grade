def replace_quotes(match):
    full_block = match.group(0)
    # キャラ名を抽出
    name_match = re.search(r'name:\s*"([^"]+)"', full_block)
    if not name_match:
        return full_block
    name = name_match.group(1)
    
    if name not in quotes_map:
        return full_block
    
    new_quotes = quotes_map[name]
    new_q_str = ', '.join(f'"{q}"' for q in new_quotes)
    
    # quotesの配列を置換
    updated = re.sub(
        r'quotes:\s*\[[^\]]+\]',
        f'quotes: [{new_q_str}]',
        full_block
    )
    return updated

# キャラクターブロックを1件ずつ処理
c = re.sub(
    r'\{[^{}]*name:\s*"[^"]+"[^{}]*quotes:\s*\[[^\]]+\][^{}]*\}',
    replace_quotes,
    c,
    flags=re.DOTALL
)

with open('script.js', 'w', encoding='utf-8') as f:
    f.write(c)

