def replace_data(match):
    full_block = match.group(0)
    name_match = re.search(r'name:\s*"([^"]+)"', full_block)
    if not name_match:
        return full_block
    name = name_match.group(1)
    
    if name not in data_map:
        return full_block
    
    entry = data_map[name]
    new_quotes = entry['quotes']
    new_desc = entry['desc']
    
    new_q_str = ', '.join(f'"{q}"' for q in new_quotes)
    
    # quotes置換
    updated = re.sub(
        r'quotes:\s*\[[^\]]*\]',
        f'quotes: [{new_q_str}]',
        full_block
    )
    # description置換
    updated = re.sub(
        r'description:\s*"[^"]*"',
        f'description: "{new_desc}"',
        updated
    )
    return updated

# キャラクターブロックを一括処理
c = re.sub(
    r'\{[^{}]*name:\s*"[^"]+"[^{}]*\}',
    replace_data,
    c,
    flags=re.DOTALL
)

with open('script.js', 'w', encoding='utf-8') as f:
    f.write(c)

