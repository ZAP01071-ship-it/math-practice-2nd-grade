# Add fallback descriptions
default_desc = 'タロットカードやエジプト9栄神に由来する恐るべきスタンド使い。'

# We use regex to iterate over the JS object entries
def repl_desc(match):
    name_line = match.group(1)
    rest_of_block = match.group(2)
    
    # Extract the actual name
    name_match = re.search(r'name:\s*"([^"]+)"', name_line)
    name = name_match.group(1) if name_match else ""
    
    desc = descriptions.get(name, default_desc)
    
    if 'description:' not in rest_of_block:
        # Insert description after quotes
        # Find where quotes ends
        rest_of_block = re.sub(r'(quotes:\s*\[.*?\])(,?)', rf'\1,\n        description: "{desc}"', rest_of_block)

    return f"{name_line}{rest_of_block}"

# The regex matches from '    { ' to the opening of the next block or end
new_text = re.sub(r'(name:\s*"[^"]+",)(.*?\})', repl_desc, text, flags=re.DOTALL)

with open('script.js', 'w', encoding='utf-8') as f:
    f.write(new_text)

